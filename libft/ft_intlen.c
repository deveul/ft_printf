int		ft_intlen(int nb)
{
	int		i;

	i = 1;
	if (nb == -2147483648)
		return (11);
	else if (nb < 0)
	{
		nb = -nb;
		i++;
	}
	while (nb > 9)
	{
		nb = nb / 10;
		i++;
	}
	return (i);
}
