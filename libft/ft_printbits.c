#include "libft.h"

void	ft_printbits(unsigned char octet)
{
	int		i;

	i = 7;
	while (i >= 0)
	{
		if (((octet >> i) & 1) == 1)
			ft_putchar('1');
		else
			ft_putchar('0');
		i--;
	}
}
